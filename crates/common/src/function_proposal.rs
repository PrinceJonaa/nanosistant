// CodingTask generation
// ═══════════════════════════════════════

fn generate_coding_task(proposal: &FunctionProposal) -> CodingTask {
    let instructions = format!(
        "Implement the deterministic function `{}` in `{}`.\n\
         \n\
         Description: {}\n\
         \n\
         Proposed signature:\n\
         ```rust\n\
         pub fn {}({}) -> {}\n\
         ```\n\
         \n\
         Implementation hint: {}\n\
         \n\
         Example queries this function should answer:\n\
         {}\n\
         \n\
         Rules:\n\
         - Function must be pure (no side effects, no I/O, no LLM calls)\n\
         - Add #[must_use] if it returns a value\n\
         - Add comprehensive unit tests\n\
         - Follow the existing style in the target file\n\
         - Register in try_deterministic_resolution() if it matches natural language",
        proposal.signature.fn_name,
        proposal.module.module_path(),
        proposal.description,
        proposal.signature.fn_name,
        proposal.signature.params.iter()
            .map(|p| format!("{}: {}", p.name, p.r#type))
            .collect::<Vec<_>>().join(", "),
        proposal.signature.return_type,
        proposal.implementation_hint,
        proposal.example_queries.iter()
            .map(|q| format!("- \"{q}\""))
            .collect::<Vec<_>>().join("\n"),
    );

    CodingTask {
        task_id: format!("OD-{}", uuid_short()),
        proposal_id: proposal.id.clone(),
        agent_role: "coder".to_string(),
        instructions,
        target_file: proposal.module.module_path(),
        verification: format!(
            "cargo test -p nstn-common {} -- --nocapture",
            proposal.signature.fn_name
        ),
        priority: if proposal.confidence >= 0.8 { TaskPriority::High }
                  else if proposal.confidence >= 0.5 { TaskPriority::Medium }
                  else { TaskPriority::Low },
    }
}

fn uuid_short() -> String {
    use std::time::{SystemTime, UNIX_EPOCH};
    let ts = SystemTime::now().duration_since(UNIX_EPOCH)
        .map(|d| d.as_millis()).unwrap_or(0);
    format!("{:X}", ts & 0xFFFFFF)
}

