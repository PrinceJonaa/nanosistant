#[cfg(test)]
mod tests {
    use super::*;
    use tempfile::TempDir;

    fn sample_proposal(id: &str) -> FunctionProposal {
        FunctionProposal {
            id: id.to_string(),
            name: "options_delta".to_string(),
            module: FunctionModule::Finance,
            description: "Calculate option delta for a call or put".to_string(),
            signature: ProposedSignature {
                fn_name: "option_delta".to_string(),
                params: vec![
                    ParamSpec { name: "s".to_string(), r#type: "f64".to_string(), description: "spot price".to_string() },
                    ParamSpec { name: "k".to_string(), r#type: "f64".to_string(), description: "strike".to_string() },
                    ParamSpec { name: "option_type".to_string(), r#type: "&str".to_string(), description: "call or put".to_string() },
                ],
                return_type: "f64".to_string(),
                is_pure: true,
                is_fallible: false,
            },
            implementation_hint: "Use normal_cdf(d1) for call, normal_cdf(d1) - 1 for put".to_string(),
            example_queries: vec![
                "what is the delta of a call option at the money".to_string(),
            ],
            supporting_episodes: vec!["e_001".to_string()],
            confidence: 0.85,
            proposed_at: Utc::now(),
            status: ProposalStatus::Pending,
            reviewer_notes: None,
        }
    }

    #[test]
    fn proposal_queue_submit_and_retrieve() {
        let tmp = TempDir::new().unwrap();
        let mut queue = ProposalQueue::new(tmp.path().join("proposals.json"));
        let id = queue.submit(sample_proposal("p001"));
        assert_eq!(id, "p001");
        assert_eq!(queue.pending().len(), 1);
    }

    #[test]
    fn proposal_approve_generates_coding_task() {
        let tmp = TempDir::new().unwrap();
        let mut queue = ProposalQueue::new(tmp.path().join("proposals.json"));
        queue.submit(sample_proposal("p002"));
        let task = queue.approve("p002", Some("looks good".to_string())).unwrap();
        assert_eq!(task.agent_role, "coder");
        assert!(task.instructions.contains("option_delta"));
        assert!(task.target_file.contains("det_finance"));
    }

    #[test]
    fn proposal_queue_save_and_load() {
        let tmp = TempDir::new().unwrap();
        let path = tmp.path().join("proposals.json");
        let mut queue = ProposalQueue::new(&path);
        queue.submit(sample_proposal("p003"));
        queue.save().unwrap();
        let mut queue2 = ProposalQueue::new(&path);
        let count = queue2.load().unwrap();
        assert_eq!(count, 1);
        assert_eq!(queue2.pending().len(), 1);
    }

    #[test]
    fn stats_tracking() {
        let tmp = TempDir::new().unwrap();
        let mut queue = ProposalQueue::new(tmp.path().join("proposals.json"));
        queue.submit(sample_proposal("p004"));
        queue.submit(sample_proposal("p005"));
        queue.approve("p004", None);
        queue.reject("p005", "already covered by det_finance::option_delta_call");
        let stats = queue.stats();
        assert_eq!(stats.approved, 1);
        assert_eq!(stats.rejected, 1);
        assert_eq!(stats.pending, 0);
    }

    #[test]
    fn tag_formalizability_numeric() {
        let signals = tag_formalizability(
            "what is 140 bpm bar duration",
            "At 140 BPM: 1.714 seconds per bar",
            "e_100",
        );
        assert!(!signals.is_empty());
    }

    #[test]
    fn module_path_resolution() {
        assert!(FunctionModule::Finance.module_path().contains("det_finance"));
        assert!(FunctionModule::Music.module_path().contains("det_music"));
        assert!(FunctionModule::New("crypto".to_string()).module_path().contains("det_crypto"));
    }

    #[test]
    fn infer_module_from_bpm() {
        let m = super::infer_module_from_query("what is the bpm of this track");
        assert_eq!(m, FunctionModule::Music);
    }

    #[test]
    fn operator_rule_proposal_roundtrips_json() {
        let proposal = OperatorRuleProposal {
            id: "orp-001".to_string(),
            rule_id: "bpm-bar-duration".to_string(),
            description: "Calculate bar duration from BPM".to_string(),
            trigger_keywords: vec!["bpm".to_string(), "bar".to_string()],
            semantic_hint: Some("bar duration from tempo".to_string()),
            formula: ProposedFormula::Arithmetic {
                expr: "60 / x * 4".to_string(),
                variables: vec!["x".to_string()],
            },
            confidence: 0.92,
            supporting_episodes: vec!["ep-001".to_string()],
            example_inputs: vec!["what is a bar at 120 bpm".to_string()],
            example_outputs: vec!["2.000 seconds".to_string()],
            proposed_at: Utc::now(),
            status: ProposalStatus::Pending,
        };
        let json = serde_json::to_string(&proposal).unwrap();
        let back: OperatorRuleProposal = serde_json::from_str(&json).unwrap();
        assert_eq!(back.id, "orp-001");
        assert_eq!(back.confidence, 0.92);
    }

    #[test]
    fn proposed_formula_static_roundtrips() {
        let formula = ProposedFormula::Static { response: "hello".to_string() };
        let json = serde_json::to_string(&formula).unwrap();
        let back: ProposedFormula = serde_json::from_str(&json).unwrap();
        match back {
            ProposedFormula::Static { response } => assert_eq!(response, "hello"),
            _ => panic!("expected Static"),
        }
    }

    #[test]
    fn proposed_formula_lookup_roundtrips() {
        let formula = ProposedFormula::Lookup {
            pairs: vec![
                ("C major".to_string(), "C D E F G A B".to_string()),
                ("A minor".to_string(), "A B C D E F G".to_string()),
            ],
        };
        let json = serde_json::to_string(&formula).unwrap();
        let back: ProposedFormula = serde_json::from_str(&json).unwrap();
        match back {
            ProposedFormula::Lookup { pairs } => assert_eq!(pairs.len(), 2),
            _ => panic!("expected Lookup"),
        }
    }
}
