//! LLM → Deterministic Function Pipeline.
//!
//! When an LLM handles a query that could have a closed-form answer,
//! the orchestrator can tag it. The Dreamer picks these up and produces
//! typed FunctionProposals. A human reviews, approves, and a Coder agent
//! writes the Rust implementation. Once merged, that query type costs
//! zero tokens forever.
//!
//! ## The asymptote
//!
//! Over time the deterministic library grows and the LLM fires less
//! often for routine computation. The LLM's remaining work becomes
//! genuinely hard: judgment, creativity, ambiguous context.
//! That's the goal — push the LLM toward what only it can do.

use std::collections::HashMap;
use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};

// ═══════════════════════════════════════
// FunctionProposal — typed IR output from the LLM/Dreamer
// ═══════════════════════════════════════

/// A proposal for a new deterministic function, generated by an LLM
/// after identifying a repeated closed-form pattern.
///
/// LLMs produce this. Humans review it. Coder agents implement it.
/// The LLM never writes the final Rust — it proposes the shape.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct FunctionProposal {
    pub id: String,
    pub name: String,
    pub module: FunctionModule,
    pub description: String,
    pub signature: ProposedSignature,
    pub implementation_hint: String,
    pub example_queries: Vec<String>,
    pub supporting_episodes: Vec<String>,
    pub confidence: f64,
    pub proposed_at: DateTime<Utc>,
    pub status: ProposalStatus,
    pub reviewer_notes: Option<String>,
}

/// Which deterministic module this function belongs to.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq, Hash)]
pub enum FunctionModule {
    Music,
    Finance,
    Data,
    Time,
    Text,
    Code,
    Geo,
    Universal,
    New(String),  // for completely new module proposals
}

impl FunctionModule {
    #[must_use]
    pub fn module_path(&self) -> String {
        match self {
            Self::Music    => "crates/common/src/det_music.rs".to_string(),
            Self::Finance  => "crates/common/src/det_finance.rs".to_string(),
            Self::Data     => "crates/common/src/det_data.rs".to_string(),
            Self::Time     => "crates/common/src/det_time.rs".to_string(),
            Self::Text     => "crates/common/src/det_text.rs".to_string(),
            Self::Code     => "crates/common/src/det_code.rs".to_string(),
            Self::Geo      => "crates/common/src/det_geo.rs".to_string(),
            Self::Universal => "crates/common/src/deterministic.rs".to_string(),
            Self::New(n)   => format!("crates/common/src/det_{}.rs", n.to_lowercase()),
        }
    }
}

/// The proposed function signature (Rust-style).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ProposedSignature {
    pub fn_name: String,
    pub params: Vec<ParamSpec>,
    pub return_type: String,
    pub is_pure: bool,       // always true for deterministic functions
    pub is_fallible: bool,   // should it return Result<T, String>?
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ParamSpec {
    pub name: String,
    pub r#type: String,
    pub description: String,
}

/// The lifecycle of a function proposal.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub enum ProposalStatus {
    /// Newly proposed, awaiting review.
    Pending,
    /// Under human review.
    UnderReview,
    /// Approved — Coder agent has been dispatched.
    Approved,
    /// Rejected — won't be implemented (reason in reviewer_notes).
    Rejected,
    /// Implemented and merged.
    Implemented { merged_at: String },
    /// Duplicate of an existing function.
    Duplicate { existing_fn: String },
}

/// Orchestrator dispatch task generated when a proposal is approved.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct CodingTask {
    pub task_id: String,
    pub proposal_id: String,
    pub agent_role: String,   // "coder"
    pub instructions: String,
    pub target_file: String,
    pub verification: String,
    pub priority: TaskPriority,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub enum TaskPriority { High, Medium, Low }

// ═══════════════════════════════════════
// Proposal Queue (stored on disk)
// ═══════════════════════════════════════

/// The queue of pending/approved function proposals.
pub struct ProposalQueue {
    proposals: Vec<FunctionProposal>,
    storage_path: std::path::PathBuf,
}

impl ProposalQueue {
    #[must_use]
    pub fn new(storage_path: impl AsRef<std::path::Path>) -> Self {
        Self {
            proposals: Vec::new(),
            storage_path: storage_path.as_ref().to_path_buf(),
        }
    }

    /// Add a new proposal.
    pub fn submit(&mut self, proposal: FunctionProposal) -> String {
        let id = proposal.id.clone();
        self.proposals.push(proposal);
        id
    }

    /// Get pending proposals.
    #[must_use]
    pub fn pending(&self) -> Vec<&FunctionProposal> {
        self.proposals.iter()
            .filter(|p| p.status == ProposalStatus::Pending)
            .collect()
    }

    /// Approve a proposal — returns the generated CodingTask.
    pub fn approve(&mut self, id: &str, notes: Option<String>) -> Option<CodingTask> {
        let proposal = self.proposals.iter_mut().find(|p| p.id == id)?;
        proposal.status = ProposalStatus::Approved;
        proposal.reviewer_notes = notes;
        Some(generate_coding_task(proposal))
    }

    /// Reject a proposal.
    pub fn reject(&mut self, id: &str, reason: &str) {
        if let Some(p) = self.proposals.iter_mut().find(|p| p.id == id) {
            p.status = ProposalStatus::Rejected;
            p.reviewer_notes = Some(reason.to_string());
        }
    }

    /// Mark as implemented.
    pub fn mark_implemented(&mut self, id: &str, merged_at: &str) {
        if let Some(p) = self.proposals.iter_mut().find(|p| p.id == id) {
            p.status = ProposalStatus::Implemented { merged_at: merged_at.to_string() };
        }
    }

    /// Stats for observability.
    #[must_use]
    pub fn stats(&self) -> ProposalStats {
        let mut stats = ProposalStats::default();
        for p in &self.proposals {
            match &p.status {
                ProposalStatus::Pending        => stats.pending += 1,
                ProposalStatus::Approved       => stats.approved += 1,
                ProposalStatus::Rejected       => stats.rejected += 1,
                ProposalStatus::Implemented{..}=> stats.implemented += 1,
                ProposalStatus::Duplicate{..}  => stats.duplicates += 1,
                ProposalStatus::UnderReview    => stats.under_review += 1,
            }
        }
        stats
    }

    /// Save to disk (JSON).
    pub fn save(&self) -> Result<(), String> {
        if let Some(parent) = self.storage_path.parent() {
            std::fs::create_dir_all(parent).map_err(|e| e.to_string())?;
        }
        let json = serde_json::to_string_pretty(&self.proposals)
            .map_err(|e| e.to_string())?;
        std::fs::write(&self.storage_path, json).map_err(|e| e.to_string())
    }

    /// Load from disk.
    pub fn load(&mut self) -> Result<usize, String> {
        if !self.storage_path.exists() { return Ok(0); }
        let json = std::fs::read_to_string(&self.storage_path)
            .map_err(|e| e.to_string())?;
        self.proposals = serde_json::from_str(&json).map_err(|e| e.to_string())?;
        Ok(self.proposals.len())
    }

    pub fn len(&self) -> usize { self.proposals.len() }
    pub fn is_empty(&self) -> bool { self.proposals.is_empty() }
}

#[derive(Debug, Clone, Default, Serialize)]
pub struct ProposalStats {
    pub pending: usize,
    pub under_review: usize,
    pub approved: usize,
    pub rejected: usize,
    pub implemented: usize,
    pub duplicates: usize,
}

// ═══════════════════════════════════════
// Formalizeability Tagger
// ═══════════════════════════════════════

/// Signals that suggest an LLM response could have been deterministic.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct FormalizabilitySignal {
    pub episode_id: String,
    pub query_preview: String,
    pub signal_type: SignalType,
    pub suggested_module: FunctionModule,
    pub confidence: f64,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub enum SignalType {
    /// Response is a number or formula result.
    NumericResult,
    /// Response is a lookup (table-driven, finite domain).
    LookupResult,
    /// Same query pattern appeared in multiple sessions.
    RepeatedPattern,
    /// Response matches a known algorithm.
    AlgorithmicPattern,
    /// User rephrased a question that was answered before.
    RephraseOfPrior,
}

/// Tag an LLM response for formalizability.
#[must_use]
pub fn tag_formalizability(query: &str, response: &str, episode_id: &str) -> Vec<FormalizabilitySignal> {
    let mut signals = Vec::new();
    let q = query.to_lowercase();
    let r = response.to_lowercase();

    // Numeric result signal
    let numeric_words = ["how many", "calculate", "what is", "convert",
                         "how much", "distance", "percent", "ratio", "rate"];
    if numeric_words.iter().any(|kw| q.contains(kw)) {
        // Check if response contains a number
        if r.chars().any(|c| c.is_ascii_digit()) {
            let module = infer_module_from_query(&q);
            signals.push(FormalizabilitySignal {
                episode_id: episode_id.to_string(),
                query_preview: query.chars().take(100).collect(),
                signal_type: SignalType::NumericResult,
                confidence: 0.75,
                suggested_module: module,
            });
        }
    }

    // Lookup result signal — short, categorical responses
    if r.split_whitespace().count() < 10
        && !r.contains("because") && !r.contains("however") {
        let module = infer_module_from_query(&q);
        if module != FunctionModule::Universal {
            signals.push(FormalizabilitySignal {
                episode_id: episode_id.to_string(),
                query_preview: query.chars().take(100).collect(),
                signal_type: SignalType::LookupResult,
                confidence: 0.65,
                suggested_module: module,
            });
        }
    }

    signals
}

fn infer_module_from_query(q: &str) -> FunctionModule {
    if q.contains("bpm") || q.contains("scale") || q.contains("chord")
        || q.contains("hz") || q.contains("note") || q.contains("beat")
        || q.contains("verse") || q.contains("lyric") { return FunctionModule::Music; }
    if q.contains("stock") || q.contains("option") || q.contains("price")
        || q.contains("return") || q.contains("volatility") || q.contains("sharpe")
        { return FunctionModule::Finance; }
    if q.contains("mean") || q.contains("median") || q.contains("average")
        || q.contains("percentile") || q.contains("correlation")
        { return FunctionModule::Data; }
    if q.contains("date") || q.contains("time") || q.contains("timezone")
        || q.contains("days") || q.contains("month") || q.contains("year")
        { return FunctionModule::Time; }
    if q.contains("word") || q.contains("text") || q.contains("sentence")
        || q.contains("syllable") || q.contains("readab")
        { return FunctionModule::Text; }
    if q.contains("version") || q.contains("semver") || q.contains("hash")
        || q.contains("base64") || q.contains("hex") || q.contains("encode")
        { return FunctionModule::Code; }
    if q.contains("distance") || q.contains("latitude") || q.contains("longitude")
        || q.contains("miles") || q.contains("km") && q.contains("coord")
        { return FunctionModule::Geo; }
    FunctionModule::Universal
}

